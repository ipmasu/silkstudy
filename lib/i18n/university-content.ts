export function localizeUniversityContent(university: University, locale: string): University {
  if (university.slug !== "hunan-university-of-technology" || (locale !== "zh" && locale !== "vi")) return university;
  const content = hutLocalized[locale];

  return {
    ...university,
    summary: content.summary,
    scholarships: [...content.scholarships],
    applicationProfile: university.applicationProfile ? {
      ...university.applicationProfile,
      rankingHighlights: [...content.rankingHighlights],
      eligibility: [...content.eligibility],
      languageRequirements: [...content.languageRequirements],
      applicationSteps: [...content.applicationSteps],
      programNotes: [...content.programNotes]
    } : undefined,
    campusLife: { ...content.campusLife }
  };
}
